
def alternate_letters():
    word = input("Please input any sentence you would like me to alternate caps for you! ")
    word = word.replace("a", "A")
    word = word.replace("e", "E")
    word = word.replace("o", "O")
    word = word.replace("u", "U")
    word = word.replace("i", "I")
    print(word)
    return
alternate_letters()
    